class SwitchCase {
    public static void main(String[] args) {
        char ch='v';
        switch(ch) {
            case 'a':
            System.out.print("Under case a");
            break;
            case 'b':
            System.out.print("Under case b");
            break;
            default:
            System.out.print("Under default case ");
        }
    }    
}

